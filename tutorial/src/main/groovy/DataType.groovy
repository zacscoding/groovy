class DataType {
    static void main(String[] args) {
        int i = 5
        long l = 100L
        float f = 10.56f
        double d = 10.5e40
        BigInteger bi = 30g
        BigDecimal bd = 3.5g

        println(i)
        println(l)
        println(f)
        println(d)
        println(bi)
        println(bd)

//        result
//        5
//        100
//        10.56
//        1.05E41
//        30
//        3.5
    }
}
